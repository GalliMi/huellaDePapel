const sizeClasses = ['size-small', 'size-medium', 'size-large'];



function loadImages() {
    gallery.innerHTML = ''; // Limpia la galería existente

    // Selecciona un número de imágenes para mostrar
    const numberOfImages = 6;
    const shuffledImages = shuffleArray([...imagePaths]);

    for (let i = 0; i < numberOfImages; i++) {
        const img = document.createElement('img');
               

        img.src = shuffledImages[i];
        img.className = getRandomSizeClass(); 
        img.style.gridRowEnd = `span ${getRandomSpan()}`;
        img.style.gridColumnEnd = `span ${getRandomSpan()}`;
        gallery.appendChild(img);
    }
}

document.getElementById('randomize-btn').addEventListener('click', function() {
    const gallery = document.getElementById('gallery');
    gallery.innerHTML = '';  // Limpia la galería existente

    const shuffledImages = shuffleArray([...imagePaths]);  // Mezcla las imágenes
    const numberOfImages = 8;  // Número de imágenes a mostrar

    for (let i = 0; i < numberOfImages; i++) {
        const img = document.createElement('img');
        img.className = getRandomSizeClass(); 
         img.style.gridRowEnd = `span ${getRandomSpan()}`; // Ajusta la altura
        img.style.gridColumnEnd = `span ${getRandomSpan()}`; // Ajusta el ancho
        img.src = shuffledImages[i];
        gallery.appendChild(img);
    }
});

// Función para mezclar el array de imágenes
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];  // Intercambia elementos
    }
    return array;
}

function getRandomSizeClass() {
    return sizeClasses[Math.floor(Math.random() * sizeClasses.length)];
} 


function getRandomSpan() {
    return Math.floor(Math.random() * 3) + 1;  // Valores entre 1 y 3
}

loadImages();

// Configurar el botón para actualizar las imágenes
document.getElementById('randomize-btn').addEventListener('click', loadImages);